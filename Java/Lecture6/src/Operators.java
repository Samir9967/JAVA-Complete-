public class Operators {
    public static void main(String[] args) {
//        + , - , * , / , %       --> Arithmetic operator
//        ==,!=,<,>,<=,>=      --> Relational Operator
//        &, |, ^, ~, <<, >>, <<<, >>>, |=, ^=, >>=, <<=, >>>=     --> Bitwise operator
//        &&, ||, !   --> Logical operator

        int a = 1;
        int b = 1;
        int c = 8;
        System.out.println(a ^ b);
        System.out.println(c << 1);
    }
}
